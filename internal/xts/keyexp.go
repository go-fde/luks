var powx = [16]byte{0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36, 0x6c, 0xd8, 0xab, 0x4d, 0x9a, 0x2f}

func subw(w uint32) uint32 {
	return uint32(sbox0[w>>24])<<24 | uint32(sbox0[w>>16&0xff])<<16 | uint32(sbox0[w>>8&0xff])<<8 | uint32(sbox0[w&0xff])
}
func rotw(w uint32) uint32 { return w<<8 | w>>24 }

// expandEnc returns the encryption round keys as a flat []byte (16 bytes per
// round key, rounds+1 of them).
func expandEnc(key []byte) []byte {
	nk := len(key) / 4
	rounds := nk + 6
	n := (rounds + 1) * 4
	enc := make([]uint32, n)
	var i int
	for i = 0; i < nk; i++ {
		enc[i] = binary.BigEndian.Uint32(key[4*i:])
	}
	for ; i < n; i++ {
		t := enc[i-1]
		if i%nk == 0 {
			t = subw(rotw(t)) ^ (uint32(powx[i/nk-1]) << 24)
		} else if nk > 6 && i%nk == 4 {
			t = subw(t)
		}
		enc[i] = enc[i-nk] ^ t
	}
	out := make([]byte, n*4)
	for j := 0; j < n; j++ {
		binary.BigEndian.PutUint32(out[j*4:], enc[j])
	}
	return out
}

// xtime multiplies by x in GF(2^8).
func xtime(b byte) byte {
	if b&0x80 != 0 {
		return (b << 1) ^ 0x1b
	}
	return b << 1
}

func gmul(a, b byte) byte {
	var p byte
	for i := 0; i < 8; i++ {
		if b&1 != 0 {
			p ^= a
		}
		b >>= 1
		a = xtime(a)
	}
	return p
}

// invMixColumn applies InvMixColumns to one 4-byte column (in place semantics
// on a 4-byte slice).
func invMixWord(w uint32) uint32 {
	b0 := byte(w >> 24)
	b1 := byte(w >> 16)
	b2 := byte(w >> 8)
	b3 := byte(w)
	o0 := gmul(b0, 0x0e) ^ gmul(b1, 0x0b) ^ gmul(b2, 0x0d) ^ gmul(b3, 0x09)
	o1 := gmul(b0, 0x09) ^ gmul(b1, 0x0e) ^ gmul(b2, 0x0b) ^ gmul(b3, 0x0d)
	o2 := gmul(b0, 0x0d) ^ gmul(b1, 0x09) ^ gmul(b2, 0x0e) ^ gmul(b3, 0x0b)
	o3 := gmul(b0, 0x0b) ^ gmul(b1, 0x0d) ^ gmul(b2, 0x09) ^ gmul(b3, 0x0e)
	return uint32(o0)<<24 | uint32(o1)<<16 | uint32(o2)<<8 | uint32(o3)
}

// expandDec builds the AESD-ready decryption schedule for ARMv8: round keys in
// forward order, with InvMixColumns applied to the inner (non-first, non-last)
// round keys, and the order reversed so AESD consumes rk[rounds]..rk[0].
func expandDec(key []byte) []byte {
	enc := expandEnc(key)
	rounds := len(key)/4 + 6
	n := (rounds + 1) // number of 16-byte round keys
	dec := make([]byte, len(enc))
	for i := 0; i < n; i++ {
		src := enc[(n-1-i)*16 : (n-1-i)*16+16]
		dst := dec[i*16 : i*16+16]
		copy(dst, src)
		if i > 0 && i < n-1 {
			for w := 0; w < 4; w++ {
				v := binary.BigEndian.Uint32(dst[w*4:])
				binary.BigEndian.PutUint32(dst[w*4:], invMixWord(v))
			}
		}
	}
	return dec
}
